def calcular_temperatura(sonidos):
    """
    Calcula la temperatura en grados Fahrenheit basada en el número de sonidos emitidos por un grillo.
    :param sonidos: Número de sonidos emitidos por el grillo en un minuto (entero)
    :return: Temperatura en grados Fahrenheit (float)
    """
    return sonidos / 4 + 40